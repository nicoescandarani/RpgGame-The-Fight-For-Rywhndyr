class Enemy {
    constructor(type, race, health, magic, strength, agility, speed, scenario, description) {
        this.race = race;
        this.type = type;
        this.health = health;
        this.strength = strength;
        this.magic = magic;
        this.agility = agility;
        this.speed = speed;
        this.scenario = scenario;
        this.description = description;
    }
}